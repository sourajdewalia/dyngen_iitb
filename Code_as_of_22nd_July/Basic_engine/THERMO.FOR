	SUBROUTINE  THERMO (PX, HX, TX, SX, AMX, L, FAR, K)
C
C	Provides thermodynamic conditions using PROCOM
C
	COMMON	/UNITS/	SI
	LOGICAL	SI

	IF (SI) GOTO 100
	DEM=1.986375
	CPG=0.250
	PSTD=1.0
	GOTO 101

100	DEN=8316.41
	CPG=1048.
	PSTD=101325.0
101	CONTINUE

	FX=0.
	IF (L.EQ.1) FX=FAR
	IF (K.EQ.1) GOTO 1
	CALL PROCOM (FX, TX, CS, AK, CP, R, PHI, H)
	GOTO 3

1	TX=HX/CPG
	DO 2 I=1,15
	CALL  PROCOM (FX, TX, CS, AK, CP, R, PHI,H)
	DELH=HX-H
	IF (ABS(DELH).LE.0.00001*HX) GOTO 3
2	TX=TX+DELH/CPG

	WRITE (8,4)
3	X=PHI-R*ALOG(PX/PSTD)
	AMX=DEM/R
	RETURN
C
C

4	FORMAT (31HNO CONVERGENCE  IN  HERMO$$S$$$)
	END

