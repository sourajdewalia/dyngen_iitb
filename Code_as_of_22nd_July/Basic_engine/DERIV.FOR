	FUNCTION DERIV (I,X)
C
C	Computes time derivatives
C
	COMMON /DYN/ ITRAN, TIME, DT, TF, JTRAN, NSTEP,TPRINT, DTPRNT
	COMMON /FOC/ FO(50,4)

	IF(JTRAN.EQ.1) GOTO 1
	DERIV=0.0
	FO(I,1)=X
	FO(I,2)=X
	FO(I,3)=DERIV
	FO(I,4)=DERIV
	RETURN
1	XO=FO(1,2)
	DERIV=(X-XO)/DT
	FO(I,1)=X
	FO(I,3)=DERIV
	RETURN
	END

